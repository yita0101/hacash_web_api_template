pub fn test_difficulty() {

    let mut bshs = "f".repeat(64);

    for i in 0 .. 50usize {
        let hs = &("0".repeat(i) + &bshs)[0..64];
        let hash: [u8; HASH_WIDTH] = hex::decode(hs).unwrap().try_into().unwrap();
        let difnum = hash_to_u32(&hash);
        let hash1 = u32_to_hash(difnum);
        let bigit = hash_to_biguint(&hash);
        let hash2 = biguint_to_hash(&bigit);
        // println!("{} {} {} {}", hex::encode(hash), hex::encode(hash1), hex::encode(hash2));
        println!("{} {} {}", hex::encode(hash1), difnum, hash_to_rateshow(&hash1, 300.0));


    }




}


